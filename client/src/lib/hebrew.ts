// Utility function to get translation
export const t = (key: string): string => {
  const keys = key.split('.');
  let result: any = he;
  
  for (const k of keys) {
    if (result && typeof result === 'object' && k in result) {
      result = result[k];
    } else {
      return key; // Return the key if translation not found
    }
  }
  
  return typeof result === 'string' ? result : key;
};

// RTL-aware date formatting
export const formatDateHe = (date: Date | string): string => {
  const d = new Date(date);
  return d.toLocaleDateString('he-IL', {
    year: 'numeric',
    month: 'long', 
    day: 'numeric'
  });
};

// RTL-aware number formatting
export const formatNumberHe = (num: number): string => {
  return num.toLocaleString('he-IL');
};

// RTL-aware currency formatting
export const formatCurrencyHe = (amount: number): string => {
  return `$${formatNumberHe(amount)}`;
};